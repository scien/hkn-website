<p style="text-align: center">
  <em>
    Eta Kappa Nu (HKN) is the electrical and computer engineering honor society of the IEEE, founded in 
    October 1904 by Maurice L. Carr at the University of Illinois at Urbana-Champaign. The organization 
    currently has around 200 student chapters and several thousand members and is headquartered in Chicago, 
    Illinois. In 2008, HKN became an organizational unit of the IEEE, making membership in the IEEE a 
    requirement for membership in HKN. The Gamma Gamma chapter of HKN was founded here at Clarkson 
    University April 29, 1950.
  </em>
</p>

<p style="text-align: center">
  <em>
    The Greek letters HKN were chosen from the 1st, 4th, and last letter of the Greek word for amber or 
    electron. In 1927, the HKN Shield and coat-of-arms was adopted. The emblem of HKN (incorporated at 
    the top of the Shield) is the Wheatstone bridge.
  </em>
</p>

<p style="text-align: center">
  <em>
    HKN was originally established for electrical engineering students but expanded to include computer
    engineering in 2000. Membership in the local chapter is offered to the top 25% of juniors and 33% of 
    seniors at each college.
  </em>
</p>

